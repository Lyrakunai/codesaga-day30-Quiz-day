# Day 30 - Quiz Day (Loops)


# 1. For Loop Tracing
for i in range(3):
    print(i)


# 2. While Loop Tracing
i = 1
while i <= 3:
    print(i)
    i += 1


# 3. Break Example
for i in range(5):
    if i == 3:
        break
    print(i)


# 4. Continue Example
for i in range(5):
    if i == 2:
        continue
    print(i)


# 5. Nested Loop Output
for i in range(2):
    for j in range(2):
        print(i, j)



